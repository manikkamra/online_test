<div class="title"><h2>About Online Examination System(OES)</h2></div>
<p>Online Examination System(OES) is a Multiple Choice Questions(MCQ) based examination system that provides an easy to use environment for both Test Conducters and Students appearing for Examination. The main objective of OES is to provide all the features that an Examination System must have, with the &quot;interfaces that doesn't Scare it's Users!&quot;. </p>

   <div class="title"><h2>Taxonomy of OES</h2></div>
   
   <p>Users of OES are classified into three categories: Administrators, Test Conductors and Students. Administrators are responsible for management of system users, subjects, tests, questions, results, system backup and recovery , etc. Test conductors are responsible for preparing subjects, tests and questions. Students are the candidates appearing for the Examination.  </p>

